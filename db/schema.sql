CREATE TABLE IF NOT EXISTS funny_videos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    video_id VARCHAR NOT NULL UNIQUE,
    rating FLOAT NOT NULL,
    vote_count BIGINT GENERATED BY DEFAULT AS IDENTITY
);

CREATE TABLE IF NOT EXISTS scary_videos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    video_id VARCHAR NOT NULL UNIQUE,
    rating FLOAT NOT NULL,
    vote_count BIGINT GENERATED BY DEFAULT AS IDENTITY
);

CREATE TABLE IF NOT EXISTS interesting_videos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    video_id VARCHAR NOT NULL UNIQUE,
    rating FLOAT NOT NULL,
    vote_count BIGINT GENERATED BY DEFAULT AS IDENTITY
);

CREATE TABLE IF NOT EXISTS general_videos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    video_id VARCHAR NOT NULL UNIQUE,
    date_added TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);